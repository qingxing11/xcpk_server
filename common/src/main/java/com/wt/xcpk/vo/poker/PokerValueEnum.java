	public static int getCardColorByPokerValue(int nPorkerValue)
	{
		if (PokerValueEnum.POKER_JOKER_SMALL == nPorkerValue)
		{
			return CardColorEnum.CARD_COLOR_NULL;
		}

		if (PokerValueEnum.POKER_JOKER_BIG == nPorkerValue)
		{
			return CardColorEnum.CARD_COLOR_NULL;
		}

		int nCardColor = (nPorkerValue % 4);
		if (0 == nCardColor)
		{
			return CardColorEnum.CARD_COLOR_CLUB;
		}
		return nCardColor;
	}
   			             		
	public static int getFaceValueByPokerValueOf(int nPorkerValue)
	{
		if (PokerValueEnum.POKER_JOKER_SMALL == nPorkerValue)
		{
			return FaceTypeEnum.FACE_TYPE_SMALL_JORKER;
		}

		if (PokerValueEnum.POKER_JOKER_BIG == nPorkerValue)
		{
			return FaceTypeEnum.FACE_TYPE_BIG_JORKER;
		}

		if ((nPorkerValue < PokerValueEnum.POKER_MIN_TYPE_VALUE) || (nPorkerValue > PokerValueEnum.POKER_MAX_TYPE_VALUE))
		{
			return FaceTypeEnum.FACE_TYPE_NULL;
		}

		if ((PokerValueEnum.POKER_A_红桃 == nPorkerValue) || (PokerValueEnum.POKER_A_方块 == nPorkerValue) || (PokerValueEnum.POKER_A_梅花 == nPorkerValue) || (PokerValueEnum.POKER_A_黑桃 == nPorkerValue))
		{
			return FaceTypeEnum.FACE_TYPE_A_VALUE;
		}

		int nCardTypeActor = (nPorkerValue - 1) / 4;
		int nCardType = nCardTypeActor + 2;
		return nCardType;
	}

